// BGR转换函数 - LCD显示器使用BGR格式
static uint16_t rgb_to_bgr(uint16_t rgb_color) {
    uint16_t r = (rgb_color >> 11) & 0x1F;  // 提取红色 (5位)
    uint16_t g = (rgb_color >> 5) & 0x3F;   // 提取绿色 (6位) 
    uint16_t b = rgb_color & 0x1F;          // 提取蓝色 (5位)
    
    // 重新组合为BGR格式
    return (b << 11) | (g << 5) | r;
}

static void disp_init(void)
{
    /*You code here*/
    LCD_Init();  // 初始化LCD硬件
    
    // 确保背光开启
    LCD_BL = 1;  
    delay_ms(100);
    
    // 使用BGR格式测试正确的颜色显示
    LCD_Clear(rgb_to_bgr(0xF800));  // 红色 -> BGR格式
    delay_ms(800);
    LCD_Clear(rgb_to_bgr(0x07E0));  // 绿色 -> BGR格式  
    delay_ms(800);
    LCD_Clear(rgb_to_bgr(0x001F));  // 蓝色 -> BGR格式
    delay_ms(800);
    
    // 清屏为白色，便于观察LVGL是否工作
    LCD_Clear(0xFFFF);  // 白色在BGR和RGB中都是0xFFFF
}

volatile bool disp_flush_enabled = true;

/* Enable updating the screen (the flushing process) when disp_flush() is called by LVGL
 */
void disp_enable_update(void)
{
    disp_flush_enabled = true;
}

/* Disable updating the screen (the flushing process) when disp_flush() is called by LVGL
 */
void disp_disable_update(void)
{
    disp_flush_enabled = false;
}

/*Flush the content of the internal buffer the specific area on the display
 *You can use DMA or any hardware acceleration to do this operation in the background but
 *'lv_disp_flush_ready()' has to be called when finished.*/

static void disp_flush(lv_disp_drv_t * disp_drv, const lv_area_t * area, lv_color_t * color_p)
{
    static uint32_t flush_count = 0;
    flush_count++;
    
    // 调试：在第一次刷新时显示明显的信号
    if(flush_count == 1) {
        // 在屏幕左上角画一个绿色方块表示disp_flush被调用了 - 使用BGR格式
        LCD_Fill(0, 0, 80, 80, rgb_to_bgr(0x07E0));  // 绿色方块，更大更明显
        delay_ms(2000);  // 显示2秒让用户确定看到
        
        // 再画一个红色方块确认颜色转换工作
        LCD_Fill(90, 0, 170, 80, rgb_to_bgr(0xF800));  // 红色方块
        delay_ms(1000);
    }
    
    if(disp_flush_enabled) {
        /*The most simple case (but also the slowest) to put all pixels to the screen one-by-one*/

        int32_t x;
        int32_t y;
        for(y = area->y1; y <= area->y2; y++) {
            for(x = area->x1; x <= area->x2; x++) {
                /*Put a pixel to the display. For example:*/
                /*put_px(x, y, *color_p)*/
                // 转换LVGL的RGB颜色到LCD的BGR格式
                LCD_Fast_DrawPoint(x, y, rgb_to_bgr(color_p->full));
                color_p++;
            }
        }

        // 可以尝试使用LCD_Color_Fill，但先用逐点绘制确保工作
        // LCD_Color_Fill(area->x1, area->y1, area->x2, area->y2, (uint16_t *)color_p);
    }

    /*IMPORTANT!!!
     *Inform the graphics library that you are ready with the flushing*/
    lv_disp_flush_ready(disp_drv);
}

